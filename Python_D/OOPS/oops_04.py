class Student:

    def __init__(delf,name,rollno,marks):
        delf.name = name
        delf.rollno = rollno
        delf.marks = marks
    
    def talk(kelf):
        print("Hello My Name is ", kelf.name)
        print("Hello My Marks are ", kelf.marks)
        print("Hello My rollno is ", kelf.rollno)

s1 = Student("Kali",101,90)
s1.talk()